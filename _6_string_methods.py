# String Methods

# isdigit()
""" Usage: Gives True if al teh characters are digits. Otherwise, False. """
result = "123".isdigit()
print(result) # True
result = "123a".isdigit()
print(result) # False

# strip()
""" Usage: Removes all the leading and trailing spaces from the string. """
result = "   hello   ".strip()
print(result) # hello

